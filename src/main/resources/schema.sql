CREATE SEQUENCE IF NOT EXISTS CUSTOMERS_SEQ START WITH 1 INCREMENT BY 50;

CREATE TABLE IF NOT EXISTS CUSTOMERS (
    ID INT DEFAULT NEXTVAL('CUSTOMERS_SEQ') PRIMARY KEY,
    NAME VARCHAR(255),
    EMAIL VARCHAR(255),
    CREATED_AT DATETIME,
    UPDATED_AT DATETIME
);

CREATE SEQUENCE IF NOT EXISTS REVIEWS_SEQ START WITH 1 INCREMENT BY 1;

CREATE TABLE IF NOT EXISTS REVIEWS (
    ID INT DEFAULT NEXTVAL('REVIEWS_SEQ') PRIMARY KEY,
    CUSTOMER_ID INT,
    COMMENT VARCHAR(255)
);

